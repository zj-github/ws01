package cn.innohub.crawler.common.beans;

import java.util.List;

public class ExtractRule {

	private List<ExtractField> fields;
	private String clazz;
	private String hostName;
	
	public String getHostName() {
		return hostName;
	}

	public void setHostName(String hostName) {
		this.hostName = hostName;
	}

	public String getClazz() {
		return clazz;
	}

	public void setClazz(String clazz) {
		this.clazz = clazz;
	}

	public List<ExtractField> getFields() {
		return fields;
	}

	public void setFields(List<ExtractField> fields) {
		this.fields = fields;
	}

	@Override
	public String toString() {
		return "ExtractRule [fields=" + fields + ", clazz=" + clazz + ", hostName=" + hostName + "]";
	}
}
